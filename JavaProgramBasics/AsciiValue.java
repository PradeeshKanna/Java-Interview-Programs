package JavaProgramBasics;

//	Java Program to find ASCII value
public class AsciiValue {

	public static void main(String[] args) {
		char myword = 'A';

		int ascii = myword;
		int ascii1 = (int) myword;

		System.out.println(ascii);		// Output:65
		System.out.println(ascii1);		// Output:65
	}

}
